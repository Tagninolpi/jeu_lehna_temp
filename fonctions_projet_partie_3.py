import math

def SigmoidProba(sigma, T, val_max=1, end_by_one=False)->tuple:
	##retourne tuples des valeurs d'1 sigmoide avc 1 pas de tps de 1
	#T : absisse du pt d'inflexion
	#sigma : pente du pt d'inflexion
	#val_max : ordonnée max à mettre ds tuple
	#end_by_one : si rajoute tjrs 1.0 à la fin du tuple si ce n'est pas le cas
	t=0
	L=[1/(1+math.exp(T*sigma))]
	while L[t]<val_max:
		t+=1
		L.append(1/(1+math.exp(-1*sigma*(t-T))))
	if end_by_one and L[t]!=1:
		L.append(1.0)
	return tuple(L)
